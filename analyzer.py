def analyze_test_result(test_id: str, answers_idx: list[int]) -> dict:
    """–°–ø—Ä–æ—â–µ–Ω–∞ –º–æ–¥–µ–ª—å –ø—ñ–¥—Ä–∞—Ö—É–Ω–∫—É: —á–∏–º –±—ñ–ª—å—à–µ –≤—ñ–¥–ø–æ–≤—ñ–¥–µ–π –∑—ñ —Å—Ç–∞—Ä—à–∏–º–∏ —ñ–Ω–¥–µ–∫—Å–∞–º–∏ (–Ω–∞–ø—Ä. '–ß–∞—Å—Ç–∫–æ–≤–æ', '–ù—ñ'),
    —Ç–∏–º –≤–∏—â–∏–π —É–º–æ–≤–Ω–∏–π –±–∞–ª –¥–ª—è –æ–∫—Ä–µ–º–∏—Ö —à–∫–∞–ª. –¶–µ –¥–µ–º–æ, –∞–ª–µ –ª–æ–≥—ñ–∫–∞ –æ–¥–Ω–æ—Ä—ñ–¥–Ω–∞ —ñ —Å—Ç–∞–±—ñ–ª—å–Ω–∞."""
    score = sum(answers_idx)
    n = max(len(answers_idx), 1)
    avg = score / n

    if avg >= 1.6:
        level = "–≤–∏—Å–æ–∫–∏–π"
    elif avg >= 0.8:
        level = "—Å–µ—Ä–µ–¥–Ω—ñ–π"
    else:
        level = "–Ω–∏–∑—å–∫–∏–π"

    summary = build_single_test_summary(test_id, score, avg, level, n)
    return {
        "score": score,
        "avg": avg,
        "level": level,
        "summary": summary,
    }

